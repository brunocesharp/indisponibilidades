-- ============================================================
-- TRIGGER DE LOG: SERVICO_MONITORADO
-- ============================================================
CREATE OR REPLACE TRIGGER TG_A_IUD_SERVICO_MONITORADO
AFTER INSERT OR UPDATE OR DELETE ON SERVICO_MONITORADO
FOR EACH ROW
BEGIN
    IF INSERTING THEN
        INSERT INTO LOG_SERVICO_MONITORADO (ID_LOG, ID_SERVICO, SGL_OPERACAO, DAT_OPERACAO)
        VALUES (SQ_LOG_SERVICO_MONITORADO.NEXTVAL, :NEW.ID_SERVICO, 'I', SYSDATE);
    ELSIF UPDATING THEN
        INSERT INTO LOG_SERVICO_MONITORADO (ID_LOG, ID_SERVICO, SGL_OPERACAO, DAT_OPERACAO)
        VALUES (SQ_LOG_SERVICO_MONITORADO.NEXTVAL, :NEW.ID_SERVICO, 'U', SYSDATE);
    ELSIF DELETING THEN
        INSERT INTO LOG_SERVICO_MONITORADO (ID_LOG, ID_SERVICO, SGL_OPERACAO, DAT_OPERACAO)
        VALUES (SQ_LOG_SERVICO_MONITORADO.NEXTVAL, :OLD.ID_SERVICO, 'D', SYSDATE);
    END IF;
END;
