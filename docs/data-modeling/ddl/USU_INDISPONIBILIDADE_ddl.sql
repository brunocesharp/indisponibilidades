-- ============================================================
-- TABELA: SERVICO_MONITORADO
-- ============================================================
CREATE TABLE SERVICO_MONITORADO (
    ID_SERVICO                   NUMBER(10)     NOT NULL,
    COD_SISTEMA                  VARCHAR2(50)   NOT NULL,
    SGL_SERVICO                  VARCHAR2(20)   NOT NULL,
    NOM_SERVICO                  VARCHAR2(150)  NOT NULL,
    DSC_URL_HEALTHCHECK          VARCHAR2(500)  NOT NULL,
    SGL_STATUS                   CHAR(1)        DEFAULT 'A' NOT NULL,
    DAT_INCLUSAO                 DATE           DEFAULT SYSDATE NOT NULL,
    NUM_CPF_USUARIO_CRIACAO      NUMBER(11)     NOT NULL,
    NUM_CPF_USUARIO_ATUALIZACAO  NUMBER(11),
    NUM_CPF_USUARIO_REMOCAO      NUMBER(11),
    DAT_CRIACAO                  DATE           DEFAULT SYSDATE NOT NULL,
    DAT_ATUALIZACAO              DATE,
    DAT_REMOCAO                  DATE,
    CONSTRAINT PK_SERVICO_MONITORADO     PRIMARY KEY (ID_SERVICO),
    CONSTRAINT UK_SERVICO_MONITORADO     UNIQUE (COD_SISTEMA),
    CONSTRAINT CK_SERVICO_MONITORADO_001 CHECK (SGL_STATUS IN ('A','I','R'))
) TABLESPACE USU_INDISPONIBILIDADE_D;

COMMENT ON TABLE  SERVICO_MONITORADO                             IS 'Servico/sistema sob monitoramento de healthcheck';
COMMENT ON COLUMN SERVICO_MONITORADO.ID_SERVICO                  IS 'Identificador unico do servico';
COMMENT ON COLUMN SERVICO_MONITORADO.COD_SISTEMA                 IS 'Codigo do sistema no inventario do Portal de Servicos';
COMMENT ON COLUMN SERVICO_MONITORADO.SGL_SERVICO                 IS 'Sigla do servico (ex.: E-TCE)';
COMMENT ON COLUMN SERVICO_MONITORADO.NOM_SERVICO                 IS 'Nome por extenso do sistema';
COMMENT ON COLUMN SERVICO_MONITORADO.DSC_URL_HEALTHCHECK         IS 'URL do endpoint de healthcheck';
COMMENT ON COLUMN SERVICO_MONITORADO.SGL_STATUS                  IS 'A=Ativo, I=Inativo, R=Removido';
COMMENT ON COLUMN SERVICO_MONITORADO.DAT_INCLUSAO                IS 'Data de inclusao no monitoramento';
COMMENT ON COLUMN SERVICO_MONITORADO.NUM_CPF_USUARIO_CRIACAO     IS 'CPF do usuario que criou o registro';
COMMENT ON COLUMN SERVICO_MONITORADO.NUM_CPF_USUARIO_ATUALIZACAO IS 'CPF do usuario que atualizou o registro';
COMMENT ON COLUMN SERVICO_MONITORADO.NUM_CPF_USUARIO_REMOCAO     IS 'CPF do usuario que removeu o registro';
COMMENT ON COLUMN SERVICO_MONITORADO.DAT_CRIACAO                 IS 'Data de criacao do registro';
COMMENT ON COLUMN SERVICO_MONITORADO.DAT_ATUALIZACAO             IS 'Data da ultima atualizacao';
COMMENT ON COLUMN SERVICO_MONITORADO.DAT_REMOCAO                 IS 'Data de remocao logica (soft delete)';

CREATE SEQUENCE SQ_SERVICO_MONITORADO START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;


-- ============================================================
-- TABELA: LOG_SERVICO_MONITORADO  (auditoria append-only)
-- ============================================================
CREATE TABLE LOG_SERVICO_MONITORADO (
    ID_LOG        NUMBER(12)  NOT NULL,
    ID_SERVICO    NUMBER(10)  NOT NULL,
    SGL_OPERACAO  CHAR(1)     NOT NULL,
    DAT_OPERACAO  DATE        DEFAULT SYSDATE NOT NULL,
    CONSTRAINT PK_LOG_SERVICO_MONITORADO     PRIMARY KEY (ID_LOG),
    CONSTRAINT CK_LOG_SERVICO_MONITORAD_001  CHECK (SGL_OPERACAO IN ('I','U','D'))
) TABLESPACE USU_INDISPONIBILIDADE_D;

COMMENT ON TABLE  LOG_SERVICO_MONITORADO              IS 'Auditoria (append-only) de operacoes sobre servicos monitorados';
COMMENT ON COLUMN LOG_SERVICO_MONITORADO.ID_LOG       IS 'Identificador unico do log';
COMMENT ON COLUMN LOG_SERVICO_MONITORADO.ID_SERVICO   IS 'Servico afetado pela operacao';
COMMENT ON COLUMN LOG_SERVICO_MONITORADO.SGL_OPERACAO IS 'I=Insert, U=Update, D=Delete';
COMMENT ON COLUMN LOG_SERVICO_MONITORADO.DAT_OPERACAO IS 'Data/hora da operacao';

CREATE SEQUENCE SQ_LOG_SERVICO_MONITORADO START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;


-- ============================================================
